# Output. –û–¥–Ω–æ —á–∏—Å–ª–æ ‚Äî –≤–µ–ª–∏—á–∏–Ω–∞ –ø–ª–æ—â–∞–¥–∏ –ø—Ä–∏–≤–µ–¥—ë–Ω–Ω–æ–≥–æ –º–Ω–æ–≥–æ—É–≥–æ–ª—å–Ω–∏–∫–∞ —Å –∞–±—Å–æ–ª—é—Ç–Ω–æ–π –ø–æ–≥—Ä–µ—à–Ω–æ—Å—Ç—å—é –Ω–µ —Ö—É–∂–µ  10‚àí16 .

N = int(input())  # –∫–æ–ª-–≤–æ –≤–µ—Ä—à–∏–Ω –ø—Ä–æ—Å—Ç–æ–≥–æ –º–Ω–æ–≥–æ—É–≥–æ–ª–Ω–∏–∫–∞

# –ø–æ —Ñ–æ—Ä–º—É–ª–µ |((x1y2-y1x1)+...+(xny1-ynx1))/2| –≤—ã—á–∏—Å–ª–∏ –ø–ª–æ—â–∞–¥—å s

