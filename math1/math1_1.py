# –î–∞–Ω–æ —á–∏—Å–ª–æ –ú. –ù–∞–π—Ç–∏ —Å—É–º–º—É –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–æ—Å—Ç—ã—Ö —á–∏—Å–µ–ª, –º–µ–Ω—å—à–∏—Ö –ú. (–ò—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å  ùëñùëõùëùùë¢ùë°)

# –≤–≤–æ–¥–∏–º N
n = int(input("n="))
# —Å–æ–∑–¥–∞–µ–º –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –ø—Ä–æ—Å—Ç—ã—Ö —á–∏—Å–µ–ª
lst = []
# –≤ k –±—É–¥–µ–º —Ö—Ä–∞–Ω–∏—Ç—å –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–µ–ª–∏—Ç–µ–ª–µ–π
k = 0
# –ø—Ä–æ–±–µ–≥–∞–µ–º –≤—Å–µ —á–∏—Å–ª–∞ –æ—Ç 2 –¥–æ N
for i in range(2, n + 1):
    # –ø—Ä–æ–±–µ–≥–∞–µ–º –≤—Å–µ —á–∏—Å–ª–∞ –æ—Ç 2 –¥–æ —Ç–µ–∫—É—â–µ–≥–æ
    for j in range(2, i):
        # –∏—â–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–µ–ª–∏—Ç–µ–ª–µ–π
        if i % j == 0:
            k = k + 1
    # –µ—Å–ª–∏ –¥–µ–ª–∏—Ç–µ–ª–µ–π –Ω–µ—Ç, –¥–æ–±–∞–≤–ª—è–µ–º —á–∏—Å–ª–æ –≤ —Å–ø–∏—Å–æ–∫
    if k == 0:
        lst.append(i)
    else:
        k = 0
# –≤—ã–≤–æ–¥–∏–º –Ω–∞ —ç–∫—Ä–∞–Ω —Å–ø–∏—Å–æ–∫
print(lst)
